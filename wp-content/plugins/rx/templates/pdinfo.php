How to Measure Your Own PD  - local (pupil distance)<br/>
<ul>
<li>The easiest way.
 
<ul>
	<li>Wear any glasses with clear lenses.</li>
	<li>Use a mirror or ask a friend or family member to mark your pupil location on the clear lenses using a dry erase marker.<br/> They must stand in front of you and be at the same height as yours.<br/> If you use a mirror make sure you stand 3ft from the mirror and your general stare is directed to the center of the glasses.</li>

	<li>Using a ruler with millimeter range print ruler</a>), measure the distance between the two dots and that’s your PD.</li>
</ul>
</li>

<li>If you don’t have in your possession glasses with clear lenses, you may measure your PD using this method.
<ul>
	<li>Place the ruler horizontally on your nose bridge, zero slightly below your right eye.</li>
	<li>Ask a friend or use your mirror to see the distance on the roller. The distance measured is your PD.</li>
</ul>	
</li>

<li>Although we don’t recommend this option. If you are unable to use the methods above you may use the general PD for: Women 62 and Men 64.</li>
</ul>
